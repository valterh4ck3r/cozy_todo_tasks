enum class CozyLanguage(val tag: String) {
    EN_US("en_US"),
    PT_BR("pt_BR"),
    ES_ES("es_ES");

    companion object {
        fun fromTag(tag: String): CozyLanguage =
            entries.firstOrNull { it.tag == tag } ?: PT_BR
    }
}

